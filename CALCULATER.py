if op=="+":
    ans=X+Y
    print('addition of 2 numbers are:', ans)
elif op=="-":
    sub=X-Y
    print('Subtraction of 2 numbers are:', sub)
elif op=="*":
    mu=X*Y
    print('multiplication of 2 numbers are:', mu)
elif op=="/":
    div=X/Y
    print('division of 2 numbers are:', div)
elif op=="%":
    MO=X%Y
    print('remainder of 2 numbers are:',MO)
